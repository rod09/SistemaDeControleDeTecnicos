    private void jTFCodigoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTFCodigoKeyReleased
        try {
            // TODO add your handling code here:
            Ferramenta ferramenta = (Ferramenta) dadosDAO.consulta(jTFCodigo.getText());
            jLFerramenta.setText(
                    ferramenta != null ? "Código já cadastrado" : "Código não cadastrado"
            );
        } catch (SQLException ex) {
            Logger.getLogger(TelaGestaoFerramenta.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTFCodigoKeyReleased

    private void setComponentes() {
        setTitle("Gestão de Ferramentas");
        setLocationRelativeTo(null);
        setResizable(false);
    }

    @Override
    protected IKey montaDado() {
        Ferramenta ferramenta = new Ferramenta();
        ferramenta.setCodigo(Utilidade.gerarUUID(jTFCodigo.getText()));
        ferramenta.setDescricao(jTFDescricao.getText());
        return ferramenta;
    }

    @Override
    protected boolean validarCampos() {

        if (jTFDescricao.getText().isEmpty()) {
            JOptionPane.showMessageDialog(
                    this,
                    "É obrigatório o preenchimento do campo 'Descricao'",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        return true;

    }

    @Override
    protected void exibirDados() {
        jTFCodigo.setText(((Ferramenta) dados.get(corrente)).getCodigo());
        jTFDescricao.setText(((Ferramenta)dados.get(corrente)).getDescricao());
    }

    @Override
    protected void setEstadoVisualizacao() {
        jTFDescricao.setEditable(false);
        jLFerramenta.setVisible(false);
    }

    @Override
    protected void setEstadoInsercao() {
        jTFDescricao.setEditable(true);
        jLFerramenta.setText("");
        jLFerramenta.setVisible(true);
    }

    @Override
    protected void setEstadoEdicao() {
        jTFDescricao.setEditable(true);
        jTFDescricao.requestFocusInWindow();
    }

    @Override
    protected void listar() {
        TelaPesquisa listagem = new TelaPesquisa(
                null,
                true,
                "Ferramentas Armazenadas",
                dadosDAO,
                "ferramenta");
        listagem.setLocationRelativeTo(null);
        listagem.setVisible(true);
    }
